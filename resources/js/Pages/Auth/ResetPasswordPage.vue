<template>
    <Head title="Redefinir senha" />
    <UPageCard class="w-full max-w-md">
        <UAuthForm
            title="Redefinir sua senha"
            description="Insira sua nova senha abaixo."
            icon="i-lucide-key"
            :fields="fields"
            :schema="schema"
            :submit="{ label: 'Redefinir Senha', size: 'lg' }"
            @submit="onSubmit"
        >
            <template #footer>
                <div class="text-center text-sm">
                    Lembrou sua senha?
                    <Link href="/login" class="font-bold text-primary-600 hover:underline">Acesse aqui</Link>
                </div>
            </template>
        </UAuthForm>
    </UPageCard>
</template>

<script setup lang="ts">
import AuthLayout from '@/Layouts/AuthLayout.vue';
import { Head, Link } from '@inertiajs/vue3';
import type { AuthFormField, FormSubmitEvent } from '@nuxt/ui';
import * as y from 'yup';

defineOptions({
    layout: AuthLayout,
});

const fields: AuthFormField[] = [
    {
        name: 'password',
        label: 'Nova Senha',
        type: 'password',
        placeholder: 'Insira sua nova senha',
        required: true,
    },
    {
        name: 'password_confirmation',
        label: 'Confirme a Nova Senha',
        type: 'password',
        placeholder: 'Confirme sua nova senha',
        required: true,
    },
];

const schema = y.object({
    password: y.string().required().min(12),
    password_confirmation: y
        .string()
        .oneOf([y.ref('password')], 'As senhas devem coincidir')
        .required(),
});

const onSubmit = (payload: FormSubmitEvent<y.InferType<typeof schema>>) => {
    console.log('Submitted', payload);
};
</script>
